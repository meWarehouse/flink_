-- =================================================== Interval Joins


SELECT
    *
FROM order_table AS ot
JOIN ship_table AS st
on ot.order_id = st.id
AND
   ot.order_time BETWEEN st.ship_time - INTERVAL '4' HOUR AND ship_time;

